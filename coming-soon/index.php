                    <h1>Sobre nós.</h1>

                    <p class="lining">A Producerta iniciou suas atividades em Pitanga  em 24/04/1992. </p>
                    <p class="lining">Tendo como um de seus fundadores Carlos Alberto Brandalise - sócio e diretor comercial.</p>
                    <p class="lining">Nos anos seguintes tornaram-se sócios da empresa Luiz Carlos Zampier e Gonçalves Silvério de Castro.</p>
                    <p class="lining">No ano 2000 colocamos nosso foco em assistência técnica – e prestação de serviços.</p>
                    <p class="lining">Em 2003 criamos a producerta cerealista, e passamos a atender o produtor do plantio à colheita.</p>
                    <p class="lining">Em 2004 ingressamos no ramo de transporte agrícola, com a aquisição da nossa primeira frota de caminhões.</p>
                    <p class="lining">Em 2006 lançamos a 1ª PRODUSHOW, que se tornou referência em novidades para o homem do campo da região central do Paraná.</p>
                    <p class="lining">Em 2008 ingressamos na atividade agrícola, com o plantio das culturas de soja, milho e trigo.</p>
                    <p class="lining">Em 2011 criamos o Programa de Agricultura de Precisão.</p>
                    <p class="lining">Em 2014 ampliamos as unidades de cereais, dobrando a sua capacidade de processamento.</p>
                    <p class="lining">Nosso objetivo é atender da melhor forma possível a demanda do produtor rural.</p>
